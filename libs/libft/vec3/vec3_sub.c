#include "vec3.h"

t_vec3	*vec3_sub(t_vec3 *u, t_vec3 *v)
{
	t_vec3	*w;

	w = malloc(sizeof(t_vec3));
	if (!w)
		return (0);
	w->x = u->x - v->x;
	w->y = u->y - v->y;
	w->z = u->z - v->z;
	return (w);
}
